HOLDOUTS_22Q = {
    "1RB 0LB 1LA 0RA",
    "1RB 1LA 0LA 0RB",
    "1RB 1LA 0LA 1RB",
    "1RB 1LA 1LA 1RB",
}

HOLDOUTS_32H = {
    lin_rado.convert(prog)
    for prog in lin_rado.HOLDOUTS
}

NOT_CONNECTED_32 = [
    '^1RB ... ..[BC] ..[BC] ..[BC] ..[BC]',
    '^1RB ..[AB] ..[AB] ..[AB] ... ...',
]


class TestLinRado(TestCase):
    def assert_progs_equal(self, other):
        self.assertEqual(
            self.progs,
            other)

    def assert_progs_count(self, count):
        self.assertEqual(
            len(self.progs),
            count)

    def run_lin_rado(self, states, colors, halt, x_limit, rejects=None):
        # pylint: disable=attribute-defined-outside-init
        self.progs = {
            prog
            for prog in
            yield_programs(
                states,
                colors,
                rejects=rejects,
                halt=halt)
            if
            run_bb(
                prog,
                x_limit=x_limit,
                check_rec=0
            ).final == 'XLIMIT'
        }

    def test_22h(self):
        self.run_lin_rado(2, 2, 1, 50)

        self.assert_progs_count(0)

    def test_22q(self):
        self.run_lin_rado(2, 2, 0, 50)

        self.assert_progs_equal(
            HOLDOUTS_22Q)

    def test_32h(self):
        self.run_lin_rado(3, 2, 1, 50, NOT_CONNECTED_32)

        self.assert_progs_count(40)

        self.assert_progs_equal(
            HOLDOUTS_32H)

    def test_32q(self):
        self.run_lin_rado(
            3, 2, 0, 126, NOT_CONNECTED_32 + [
                prog.replace('1RH', '...')
                for prog in HOLDOUTS_32H
            ],
        )

        self.assert_progs_equal(
            HOLDOUTS_32Q)

        self.assert_progs_count(609)


